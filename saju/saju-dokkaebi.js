  // 오행 분포 → 직설 묘사(동적)
  function ohengParagraph(res) {
    var o = res.oheng, max = Math.max.apply(null, o);
    var strong = [], none = [];
    for (var i = 0; i < 5; i++) {
      if (o[i] === max && max > 0) strong.push(i);
      if (o[i] === 0) none.push(i);
    }
    var s = "네 안엔 " + strong.map(function (i) { return OHENG[i].name; }).join("·") + "의 기운이 제일 세다. " +
      "말하자면 " + OHENG[strong[0]].strong + ", 그게 남보다 도드라진다는 뜻이다. ";
    if (none.length) {
      s += "대신 " + none.map(function (i) { return OHENG[i].name; }).join("·") + " 기운은 아예 안 보인다. " +
        none.map(function (i) { return OHENG[i].lack; }).join(", ") + " — 그게 부족하니, 살면서 두고두고 아쉬운 대목이 된다. ";
    } else {
      s += "다섯 기운이 큰 결핍 없이 고루 섞여, 어느 한쪽으로 치우치지 않고 상황 따라 균형을 잡는 편이다. ";
    }
    var yy = res.yinYang;
    if (yy.yang > yy.yin) s += "게다가 양(陽)의 기운이 세서, 가만있기보다 밖으로 뻗고 부딪치며 사는 팔자다.";
    else if (yy.yin > yy.yang) s += "게다가 음(陰)의 기운이 세서, 나서기보다 안에서 살피고 다지며 사는 팔자다.";
    else s += "음과 양이 팽팽해서, 나설 때와 물러설 때를 아는 균형을 타고났다.";
    return s;
  }

