function faqSection(qas) {
  const body = qas.map(function (qa) {
    return "      <h3>Q. " + qa[0] + "</h3>\n      <p>" + qa[1] + "</p>";
  }).join("\n");
  return '    <section class="card info faq-section">\n' +
    "      <h2>자주 묻는 질문</h2>\n" + body + "\n    </section>\n";
}

function faqJsonLd(qas) {
  const obj = {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": qas.map(function (qa) {
      return { "@type": "Question", "name": qa[0], "acceptedAnswer": { "@type": "Answer", "text": qa[1] } };
    })
  };
  return '  <script type="application/ld+json">\n  ' + JSON.stringify(obj) + "\n  </script>\n\n";
}

let n = 0;
Object.keys(FAQ).forEach(function (slug) {
  const file = path.join(ROOT, slug, "index.html");
  if (!fs.existsSync(file)) { console.log("SKIP(없음):", slug); return; }
  let html = fs.readFileSync(file, "utf8");
  if (html.indexOf("faq-section") !== -1) { console.log("이미 있음:", slug); return; }

  // FAQ 섹션을 </main> 앞에 삽입
  html = html.replace("  </main>", faqSection(FAQ[slug]) + "  </main>");
  // FAQPage 구조화 데이터를 AdSense 스크립트 앞에 삽입
  html = html.replace("  <!-- Google AdSense", faqJsonLd(FAQ[slug]) + "  <!-- Google AdSense");

  fs.writeFileSync(file, html);
  n++;
  console.log("FAQ 추가:", slug, "(" + FAQ[slug].length + "문항)");
});
console.log("\n완료:", n, "개 페이지");
